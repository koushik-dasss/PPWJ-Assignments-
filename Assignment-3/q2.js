// A fruit vendor arranges the fruits on his stall in alphabetical order
// so that customers can find them easily. However, sometimes he
// prefers to display them in reverse order (for decoration purposes).
// You are given an array of fruit names. Your task is to:
// - Sort the fruits in alphabetical order.
// - Reverse the order and display the result again.
// - Print both arrangements to the console.
let fruits = ["cherries" , "bananas" , "apples" , "papayas" , "mangoes"] ; // fruits in random order 
console.log("Original array is : " , fruits);
console.log("Sorted array is :" , fruits.sort()); // sorted array 
console.log("Reverse array is : " , fruits.reverse()); // reversed array 



